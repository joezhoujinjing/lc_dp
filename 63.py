class Solution(object):
	def uniquePath(self,obstacleGrid):
		self.count=0
		grid=obstacleGrid
		cache={}
		def dp(grid):
			#goal reached
			if len(grid)==1 and len(grid[0])==1 and grid[0][0]==0:
				self.count+=1
			#dead end
			elif grid[0][0]==1:
				self.count+=0
			#neither case
			else:
				if len(grid[0])>1 and grid[0][1]==0:
					newGrid=[[None]*len(grid[0])]*len(grid)
					for i in range(len(newGrid)):
						newGrid[i]=grid[i][1:]
					dp(newGrid)
				if len(grid)>1 and grid[1][0]==0:
					newGrid=grid[1:]
					dp(newGrid)
		dp(grid)
		return self.count

	def uniquePath2(self,obstacleGrid):
		if obstacleGrid[-1][-1]==1 or obstacleGrid[0][0]==1:
			return 0
		m=len(obstacleGrid)
		n=len(obstacleGrid[0])
		dp=[0]*n
		dp[0]=1
		for i in xrange(m):
			for j in xrange(n):
				if obstacleGrid[i][j]==1:
					dp[j]=0
				elif j!=0:
					dp[j]=dp[j]+dp[j-1]
		return dp[n-1]
